const Featured = ({ data = [] }) => {
  // Dummy data to display when no data comes from database
  const dummyFeaturedProjects = [
    {
      title: "E-commerce Platform",
      description: "A full-stack e-commerce solution built with React, Node.js, and MongoDB. Features include user authentication, product management, shopping cart, and payment processing.",
      summary: "Modern e-commerce platform with React and Node.js",
      image: { url: "/demo.png", alt: "E-commerce Platform" },
      github: "https://github.com/example/ecommerce",
      external: "https://ecommerce-demo.com",
      tech: ["React", "Node.js", "MongoDB", "Stripe", "Redux"]
    },
    {
      title: "Task Management App",
      description: "A collaborative task management application with real-time updates, team collaboration, and progress tracking. Built with modern web technologies.",
      summary: "Collaborative task management with real-time updates",
      image: { url: "/demo.png", alt: "Task Management App" },
      github: "https://github.com/example/task-manager",
      external: "https://task-manager-demo.com",
      tech: ["Vue.js", "Express", "Socket.io", "PostgreSQL", "JWT"]
    },
    {
      title: "AI Chat Assistant",
      description: "An intelligent chatbot powered by OpenAI's GPT model, integrated with a modern web interface. Features conversation history, context awareness, and customizable responses.",
      summary: "AI-powered chatbot with GPT integration",
      image: { url: "/demo.png", alt: "AI Chat Assistant" },
      github: "https://github.com/example/ai-chat",
      external: "https://ai-chat-demo.com",
      tech: ["React", "OpenAI API", "Node.js", "WebSocket", "Tailwind CSS"]
    }
  ];

  // Use dummy data if no real data is provided
  const featuredProjects = data && data.length > 0 ? data : dummyFeaturedProjects;

  const revealTitle = useRef(null);
  const revealProjects = useRef([]);
  const prefersReducedMotion = usePrefersReducedMotion();

  useEffect(() => {
    if (prefersReducedMotion) {
      return;
    }

    sr.reveal(revealTitle.current, srConfig());
    revealProjects.current.forEach((ref, i) => sr.reveal(ref, srConfig(i * 100)));
  }, [prefersReducedMotion]);

  return (
    <section id="projects">
      <h2 className="numbered-heading" ref={revealTitle}>
        Some Things I've Built
      </h2>

      <StyledProjectsGrid>
        {featuredProjects &&
          featuredProjects.map((project, i) => {
            const { external, title, tech, github, image, description, summary } = project;

            return (
              <StyledProject key={i} ref={el => (revealProjects.current[i] = el)}>
                <div className="project-content">
                  <div>
                    <p className="project-overline">Featured Project</p>

                    <h3 className="project-title">
                      {external ? (
                        <a href={external}>{title}</a>
                      ) : (
                        <span>{title}</span>
                      )}
                    </h3>

                    <div className="project-description">
                      {description || summary}
                    </div>

                    {tech && tech.length > 0 && (
                      <ul className="project-tech-list">
                        {tech.map((techItem, i) => (
                          <li key={i}>{techItem}</li>
                        ))}
                      </ul>
                    )}

                    <div className="project-links">
                      {github && (
                        <a href={github} aria-label="GitHub Link">
                          <Icon name="GitHub" />
                        </a>
                      )}
                      {external && (
                        <a href={external} aria-label="External Link" className="external">
                          <Icon name="External" />
                        </a>
                      )}
                    </div>
                  </div>
                </div>

                <div className="project-image">
                  <a href={external ? external : github ? github : '#'}>
                    {image && image.url && (
                      <Image src={image.url} alt={image.alt || title} width={700} height={400} className="img" />
                    )}
                  </a>
                </div>
              </StyledProject>
            );
          })}
      </StyledProjectsGrid>
    </section>
  );
};

Featured.propTypes = {
  data: PropTypes.array,
};

export default Featured;
